function get_tracking_url_for_landing($dataLDSetting,$gl_source = false){
	if(!$gl_source){
		$gl_source = array(
			'entry_row'=>'0',
			'gclid'=>'0',
			'sites_page_id'=>'0',
			'has_gclid'=>'0',
			'is_mobile'=>'0',
		);
	}
	$page_id = $dataLDSetting['id'];
	$form_page_id = $dataLDSetting['mailignList_contentPageID'];
	$tracking_form_data = false;
	if($form_page_id!='' && $form_page_id!='0'){
		$tracking_form_data = get_data_estimate($form_page_id);
	}
	$user_ip = $_SERVER[REMOTE_ADDR];
	$tmp = time();
	$tracking_id = $user_ip.$tmp;
	$tracking_id = str_replace(".","",$tracking_id);	
	$tracking_arr = array("unk"=>UNK,"cat_f"=>'0',"cat_s"=>'0',"cat_spec"=>'0',"page_id"=>$page_id,"page_type"=>'2',"page_m"=>'land',"ip"=>$user_ip,'gclid'=>$gl_source['gclid'],'has_gclid'=>$gl_source['has_gclid'],'is_mobile'=>$gl_source['is_mobile'],"uniq_track"=>$tracking_id,"s_term"=>"");
	if(isset($_GET['ld'])){
		$tracking_arr['page_type'] = '3';
	}
	if($tracking_form_data){
		if(isset($tracking_form_data['primeryCat'])){
			$tracking_arr['cat_f'] = $tracking_form_data['primeryCat'];
		}
		if(isset($tracking_form_data['subCat'])){
			$tracking_arr['cat_s'] = $tracking_form_data['subCat'];
		}
		if(isset($tracking_form_data['cat_spec'])){
			$tracking_arr['cat_spec'] = $tracking_form_data['cat_spec'];
		}
	}
	$tracking_url_q = "t=1"; 
	foreach($tracking_arr as $key=>$val){
		$tracking_url_q.="&$key=$val";
	}
	$tracking_url = "https://ilbiz.co.il/c_tracking/?".$tracking_url_q;
	$form_tracking_url = $tracking_url."&f_sent=1";	
	return array("page"=>$tracking_url,"form"=>$form_tracking_url);
}

